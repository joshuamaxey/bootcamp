//! Memory

// Memory stores the state of our programs and all of our code

//! RAM

// RAM (random access memory) is a hardware component that stores data corresponding to programs that are currently in use including the operating system and background processes.

// RAM is fast and requires an active power source in order for data to persist.

// Hard drives and solid state drives offer slower, long-term data storage that persists without a power source.

// Both RAM and hard drives store data as long series' of bits (1s and 0s) that can be read and written.

